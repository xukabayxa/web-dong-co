                                <p class="Normal">AES là một doanh nghiệp có nhiều kinh nghiệm trong sản xuất, phân phối
                                    điện trên thế giới. Tại Việt Nam, tập đoàn này quan tâm tới việc đầu tư vào chuỗi dự
                                    án khí Sơn Mỹ do Tổng công ty Khí Việt Nam (PV GAS) làm chủ đầu tư. Tháng 11/2017,
                                    PV GAS và AES đã ký bản ghi nhớ về hợp tác tại dự án kho cảng LNG Sơn Mỹ.</p>
                                <p class="Normal">Ngoài ra, AES cùng với Posco Energy (Hàn Quốc) và China Investment
                                    Corporation (Trung Quốc) đã phát triển dự án Nhiệt điện Mông Dương 2 có công suất
                                    1.200 MW. Dự án đã hoàn thành và được đưa vào vận hành thương mại từ tháng
                                    4/2015.</p>
                                <p class="Normal">Cũng trong khuôn khổ chuyến thăm này, Bộ Công Thương và Bộ Ngoại giao
                                    Mỹ đã ký biên bản ghi nhớ hợp tác (MOU) đối tác hợp tác năng lượng toàn diện, mở ra
                                    kỳ vọng&nbsp;xây dựng lộ trình hợp tác trong từng lĩnh vực năng lượng cụ thể.</p>
                                <p class="Normal">Tháng trước, một dự án điện khí hơn 5 tỷ USD cũng được tỉnh Bình Thuận
                                    chấp thuận cho phép Công ty Đầu tư và quản lý Quỹ Energy Capital Việt Nam (ECV) đầu
                                    tư. Dự án này dự kiến hoàn thành vào 2025 với tổng công suất 3.200 MW, sử dụng
                                    nguyên liệu LNG nhập khẩu từ Mỹ.</p>
                                <p class="Normal">Theo số liệu của Cục Điện lực và năng lượng tái tạo (Bộ Công Thương),
                                    dự kiến đến năm 2020, công suất nhiệt điện khí là gần 9.000 MW, sẽ tăng lên 19.000
                                    MW 10 năm sau đó. Điện khí sẽ chiếm khoảng 14% tổng công suất toàn hệ thống điện đến
                                    năm 2030 và để đủ 22 tỷ m3 khí sản xuất 19.000 MW, Việt Nam sẽ phải nhập khẩu 50%
                                    khí LNG.</p>
