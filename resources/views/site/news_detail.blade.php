                        <h1 class="heading">Tập đoàn Mỹ đầu tư nhà máy điện khí 5 tỷ USD</h1>
                        <div class="entry-content">
                            <p class="description">Thủ tướng đồng ý giao Tập đoàn AES (Mỹ) làm chủ đầu tư dự án điện Sơn
                                Mỹ 2 tại Bình Thuận theo hình thức BOT trị giá 5 tỷ USD.</p>
                            <article class="content_detail fck_detail width_common block_ads_connect">
                                <p class="Normal">Văn bản Thủ tướng chấp thuận giao Tập đoàn AES (Mỹ) làm chủ đầu tư dự
                                    án nhà máy điện khí Sơn Mỹ 2 (Bình Thuận) theo hình thức hợp đồng xây dựng – kinh
                                    doanh – chuyển giao (BOT),&nbsp;vừa được Bộ trưởng Công Thương trao cho tập đoàn này
                                    nhân chuyến thăm, làm việc tại Mỹ.</p>
                                <p class="Normal">Hợp đồng xây dựng nhà máy điện này có tổng trị giá khoảng hơn 5 tỷ
                                    USD, khi chính thức đi vào vận hành sẽ tạo ra nhu cầu nhập khẩu khí hóa lỏng LNG từ
                                    Mỹ lên tới gần 2 tỷ USD mỗi năm.</p>
                                <p class="Normal">Theo Bộ Công Thương, việc chấp thuận giao cho Tập đoàn AES thực hiện
                                    dự án điện khí 5 tỷ USD sẽ góp phần hài hòa cán cân thương mại với Mỹ.</p>
                                <p class="Normal">Dự án nhiệt điện khí Sơn Mỹ 2 nằm trong tổ hợp chuỗi dự án nhiệt điện
                                    Sơn Mỹ (gồm Sơn Mỹ 1, 2 và 3) tại tỉnh Bình Thuận có tổng công suất 4.000 MW. Ước
                                    tính mỗi năm nhà máy này tiêu thụ gần 0,6 triệu tấn LNG, dự kiến vận hành vào năm
                                    2024.</p>
